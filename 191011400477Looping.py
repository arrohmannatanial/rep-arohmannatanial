#Contoh pengulangan for sederhana
angka = [1,2,3,4,5]
for x in angka:
    print(x)

#Contoh pengulangan for
buah = ["nanas", "apel", "jeruk"]
for makanan in buah:
    print ("Saya suka makan", makanan)
#Contoh penggunaan While Loop
count = 0
while (count < 9):
    print ("The count is: ", count)
    count = count + 1

print ("Good bye!")